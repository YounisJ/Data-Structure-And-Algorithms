package dsa;

import java.util.Arrays;

public class InsertionSort {
    public static void main(String[] args) {
        
        int[] d={6,3,8,1,2,7,9}; 
        int item=2;
        int k=0,loc=0;
        
        while(k<d.length)
        {
            if(item==d[k])
            {
                
                loc=k;
            }
        }
        
        
        
        System.out.println(loc);
        
        }
    
}
